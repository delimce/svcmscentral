 <p>Esta secci&oacute;n es&nbsp; una de las m&aacute;s importantes de la p&aacute;gina. Aqui usted puede armar todo el contenido de su
 sitio web agregando infinitas categor&iacute;as y art&iacute;culos, modificarlos y borrarlos cuando usted desee. </p>
 <p>Las categor&iacute;as son como carpetas, que puede contener otras <strong>categor&iacute;as</strong> (subcategorias)
 o <strong>art&iacute;culos</strong> (las p&aacute;ginas con la
 informaci&oacute;n, fotos, documentos, etc). </p>
 <div class="help-subtitulo">Entendiendo los niveles de categor&iacute;as y art&iacute;culos</div>
 <p>Nuestro &aacute;rbol de contenidos le permite crear&nbsp; una estructura 100% l&oacute;gica&nbsp; de 3 niveles de profundidad.
 Esto significa que usted podr&aacute; crear Categor&iacute;as s&oacute;lo hasta un tercer nivel de profundidad, y art&iacute;culos dentro de cualquiera
 de estos niveles. Esto le permitir&aacute; en la mayor&iacute;a de los casos poder armar toda la estructura de contenido necesaria para
 su web site. <strong>Usted no
 podr&aacute; crear
 categor&iacute;as
 m&aacute;s
 profundas que 3er nivel;</strong> Es
 decir que usted s&oacute;lo puede jugar con
 Categor&iacute;as I, II y II y agregar art&iacute;culos en cualquiera de estos niveles, pero no m&aacute;s profundo que
 eso.</p>
 <p>Si usted agrega un art&iacute;culo a una categor&iacute;a,&nbsp; ya no podr&aacute; agregar sub categor&iacute;as, esto
 quiere decir
que&nbsp; las categor&iacute;as pueden contener <strong>&Oacute; art&iacute;culos &Oacute; subcategor&iacute;as</strong> dentro
de ellas. En cualquier caso, es <strong>altamente recomendable</strong> que usted organice primero su contenido en fr&iacute;o
antes de transcribirlo
al sistema, jugando con las posibilidades que svCMS le ofrece.</p>
