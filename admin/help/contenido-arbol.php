<p class="help-subtitulo">Qúe significan los íconos en el árbol de contenidos</p>
<p>Al lado de cada categoría o artículo usted podrá ver una serie de íconos, cada uno de ellos representa una acción que
puede realizar sobre esa categoría  o artículo; si deja el ratón por sobre el ícono por un breve instante, un pequeño texto
de ayuda le recordará la función del ícono.</p>
<p class="help-subtitulo2">Acciones en categor&iacute;as o sub categorias:</p>
<p><img src="../icon/icon-cat-add.gif" width="16" height="16" align="absmiddle"> <strong>Agregar categor&iacute;a:</strong> Agrega una categor&iacute;a
dentro de la categor&iacute;a donde usted le de click a ese &iacute;cono. Para agregar una categor&iacute;a nivel I , hay un bot&oacute;n que dice &quot;Agregar
categor&iacute;a de primer nivel&quot; con el mismo &iacute;cono de agregar categor&iacute;a.</p>
<p>Al agregar una categor&iacute;a usted ver&aacute; un <a href="javascript:;"title="popup es una ventana flotante!" class="help-link-explicativo">popup</a> que le pide los datos de la misma:</p>
<p class="help-p2"><strong>Nombre de la Categor&iacute;a:</strong> Ovbio</p>
<p class="help-p2"><strong>Descripci&oacute;n de la categor&iacute;a:</strong> sirve como res&uacute;men explicativo de qu&eacute; hay dentro de la categor&iacute;a. Si su dise&ntilde;o no incluye
listados de categor&iacute;as y sub categor&iacute;as, usted puede dejar este espacio en blanco&nbsp; o&nbsp; usarlo referencialmente.</p>
<p class="help-p2"><strong>Imagen de categor&iacute;a:</strong> si su sistema utiliza listado de categor&iacute;as o subcategor&iacute;as con&nbsp; im&aacute;genes
o si su p&aacute;gina principal que lista las categor&iacute;as lleva una im&aacute;gen tem&aacute;tica, este es el&nbsp; lugar
para subirla. Tenga en cuenta las observaciones para el uso de <a href="imagenes/index.html" target="_blank">im&aacute;genes en el web
site</a>.</p>
<p class="help-p2"><strong>Categor&iacute;a alimentable por usuarios:</strong> Elija si esta categor&iacute;a es <a href="contenido-categorias-alimentables.php" title="ver ayuda sobre este tema">susceptible de adiciones de art&iacute;culos por los usuarios</a> registrados de su sitio web.</p>
<p><img src="../icon/icon-cat-delete.gif" width="16" height="16" align="absmiddle"> <strong>Borrar categor&iacute;a:</strong> Borra
la categor&iacute;a y TODA la informaci&oacute;n que esta contenga, incluyendo art&iacute;culos y sub categor&iacute;as, funciona igual que borrar una
carpeta de Windows. Proceda con cautela ya que esta acci&oacute;n es irreversible, no existe funci&oacute;n de &quot;Deshacer / Undo&quot; para estas
acciones.</p>





