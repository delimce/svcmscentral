 <p><strong>Si usted desea ver todos los usuarios</strong> que est&aacute;n en su base de datos, haga una <strong>b&uacute;squeda
 vac&iacute;a</strong>, es decir sin seleccionar
 ning&uacute;n criterio. Para encontrar diferentes tipos de usuarios puede realizar infinitos tipos de b&uacute;squeda dependiendo
 de sus necesidades y su creatividad. Por ejemplo, puede buscar todos los registros que sean de categoría "Proveedores" y
 que su telefono contenga 0414 (clientes movistar).</p>
 <p><strong>Para agilizar la traida de datos</strong> en caso de grandes cantidades de usuarios,&nbsp; usted puede seleccionar<em> qu&eacute; campos
 desea ver en el&nbsp; listado</em> (ej. nombre, email, tel&eacute;fono); as&iacute; si solo desea ver los correos electr&oacute;nicos de sus clientes,
 seleccione email como campo que desea ver en el listado.</p>
 <p>Para encontrar los <strong>nuevos usuarios inscritos por la p&aacute;gina web</strong>, que&nbsp; todav&iacute;a no est&aacute;n activos,
 busque en <strong>estatus</strong> &quot;<em>Inactivo</em>&quot; y <strong>origen</strong> &quot;<em>Inscrito por la p&aacute;gina
 web</em>&quot;. </p>
