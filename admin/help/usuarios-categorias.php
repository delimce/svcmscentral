<p>Usted puede organizar a sus&nbsp; usuarios en categor&iacute;as para facilitar luego&nbsp; la b&uacute;squeda y el env&iacute;o de informaci&oacute;n. Cyuando se tienen pocos usuarios, &eacute;sto parece ser innecesario pero cuando&nbsp; ya su base de datos comienza a crecer, se dar&aacute; cuenta de que&nbsp; era mejor tener organizados a sus&nbsp; contactos. </p>
<p>Usted dispone de 5 Categor&iacute;as generales dentro de las cuales puede crear ilimitadas sub categor&iacute;as. Nosotros llamamos a esas categor&iacute;as generales: grupos o &quot;dimensiones&quot; ya que&nbsp; usted puede colocar a su&nbsp;usuario en una sub categor&iacute;a de cada una de estas categor&iacute;as generales.</p>

<p>Antes de que&nbsp; usted se comience a tirar los&nbsp; pelos por lo complicado que esto suena, <strong>comencemos con un&nbsp; ejemplo</strong>:</p>
<p><img src="usuarios-categorias.jpg" width="888" height="623" border="1"></p>

<p>En el GRUPO1&nbsp; tengo a mis&nbsp; usuarios organizados por el TIPO de RELACION que tienen con la empresa. Yo puedo tener a un cliente dentro de una de estas categor&iacute;as en este GRUPO, pues estas categor&iacute;as est&aacute;n pensadas para ser mutuamente excluyentes. </p>


<p>En ste caso, el grupo 2 lo tengo asignado para  &quot;sexo&quot; . Tengo  &quot;hombres&quot; y &quot;mujeres&quot; como categor&iacute;as. Y&nbsp;podr&eacute; meter a mi usuario&nbsp; O en hombres O&nbsp; en mujeres. pero no en ambos.</p>


<p>As&iacute; sucesivamente tengo los Grupos&nbsp; 2 3 4 y 5 en los que puedo crear otras categor&iacute;as mutuamente excluyentes.</p>

<p>De esta forma&nbsp; yo podria tener un&nbsp; usuario que cumpla con estas caracter&iacute;sticas:</p>
<p>1.-&nbsp; Que sea Cliente</p>
<p>2.- Que sea Hombre</p>
<p>3.- Que sea Soltero</p>
<p>En la&nbsp; p&aacute;gina de b&uacute;squeda de usuarios, yo puedo&nbsp; realizar&nbsp; este tipo de&nbsp; b&uacute;squeda para&nbsp; as&iacute; poder filtrar y encontrar al tipo de cliente al que me quiero dirigir.</p>
<p><strong>Sea cuidadoso,  cree buenas categor&iacute;as (que le sean &uacute;tiles) y procure organizar a sus contactos&nbsp; a medida que vayan cre&aacute;ndose.</strong></p>
