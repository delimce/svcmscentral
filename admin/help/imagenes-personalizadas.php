<div class="td-titulo-popup">Subiendo Im&aacute;genes personalizadas al cuerpo de sus art&iacute;culos o productos</div>
<div class="textohelp">
 <p class="help-boton-volver"><a href="index.php"><img src="../icon/icon-left.gif" width="16" height="16" border="0" align="absmiddle"> Volver
   al índice de la ayuda</a></p>
 <p>Adem&aacute;s de las im&aacute;genes standard, Usted puede utilizar im&aacute;genes adicionales y diagramarlas dentro del espacio para el texto del art&iacute;culo o producto, copi&aacute;ndolas y peg&aacute;ndolas desde otro servidor (o el suyo propio). </p>
 <p>Si usted necesita colocar m&aacute;s de una&nbsp; im&aacute;gen dentro del cuerpo del art&iacute;culo o producto, debe&nbsp;agregarlas usando el bot&oacute;n <img src="../icon/file_list_icons/icon_gif.gif" alt="icono imagen"> en el editor de contenido. El &uacute;nico detalle es que <strong>la imagen debe estar&nbsp; &quot;hospedada&quot; en un servidor</strong>, ya que al&nbsp; agregarla,&nbsp; el programa nos va a pedir la &quot;direcci&oacute;n&quot; de la imagen.</p>
 <p>Aqu&iacute; le mostraremos dos m&eacute;todos para colocar las imagenes y luego&nbsp; introducirlas en su editor de texto y luego poder diagramarlas de la manera que desee.</p>






 <p class="help-subtitulo">1.- Utilizando las im&aacute;genes adicionales del mismo&nbsp; art&iacute;culo / producto (cargadas por usted mismo).</p>
 

<p class="help-boton-video"><a href="http://screencast.com/t/yhLWD5pZXQ2" target="_blank"><img src="../icon/video.png" alt="ver video" width="32" height="32" border="0" align="absmiddle"></a> Vea el Video Primero</p>
<p >1.- Prepare &nbsp;y optimice su imagen. Vaya al editor de contenidos y edite el art&iacute;culo o producto donde desea agregar la imagen. <br>
  <strong>C&aacute;rguela como imagen adicional En el panel &quot;Im&aacute;genes Adicionales&quot; (en el caso de un art&iacute;culo) o &quot;Im&aacute;genes del Producto&quot; (si es un producto)</strong></p>






<p><a href="imagenes-personalizadas1.jpg" target="_blank"><img src="imagenes-personalizadas1.jpg" alt="Click para agrandar!" width="726" height="184" border="1"></a> </p>
 <p>2.- <strong>Haga Click en la imagen</strong> . Esta abrir&aacute; en una&nbsp; ventana nueva.</p>
<p><a href="imagenes-personalizadas2.jpg" target="_blank"><img src="imagenes-personalizadas2.jpg" alt="Click para agrandar!" width="726" height="294" border="1"></a></p>
 <p>3.- En la Ventana nueva, copie el link de la imagen de la barra de direcciones como muestra la imagen superior.</p>
 <p>4.-&nbsp; En el editor de texto de su&nbsp; art&iacute;culo, ubique el cursor en&nbsp; la&nbsp; parte del texto donde usted desea colocar su imagen y luego seleccione el bot&oacute;n <img src="../icon/file_list_icons/icon_gif.gif" alt="icono imagen">. Esto abrir&aacute; la interfase de manejo de im&aacute;genes:</p>
 <p><a href="imagenes-personalizadas3.jpg" target="_blank"><img src="imagenes-personalizadas3.jpg" alt="Click para agrandar!" width="724" height="465" border="1"></a></p>
 <p>5.- <strong>Pegue (CTRL V) la direcci&oacute;n de la imagen en el campo &quot;Direcci&oacute;n de la imagen&quot;</strong> (Escriba tambi&eacute;n una descripci&oacute;n para su imagen). </p>
 <p>6.-Luego <strong>seleccione la pesta&ntilde;a &quot;Aspecto&quot;</strong> para poder editar la&nbsp; apariencia de su imagen insertada. Si usted no ha aprendido todav&iacute;a a <a href="imagenes/index.html" target="_blank">optimizar las imagenes para su sitio&nbsp; web</a>, quiz&aacute;s  coloque im&aacute;genes demasiado grandes o demasiado peque&ntilde;as. Entonces es probable que necesite modificar las &quot;Dimensiones&quot; de la imagen que est&aacute; insertando para que no destruya la&nbsp; diagramaci&oacute;n de su p&aacute;gina.</p>
<p><a href="imagenes-personalizadas4.jpg" target="_blank"><img src="imagenes-personalizadas4.jpg" alt="cuadro de edicion de imagenes 2" border="1"></a></p>
<p>Al finalizar su edici&oacute;n, podr&aacute; ver la imagen ya&nbsp; colocada dentro de&nbsp; su cuadro de edici&oacute;n de texto:</p>
<p><a href="imagenes-personalizadas5.jpg" target="_blank"><img src="imagenes-personalizadas5.jpg" alt="edicion de texto. como se ve la imagen"></a></p>
<p>Ya su imagen est&aacute; colocada dentro del cuerpo de su&nbsp; art&iacute;culo. Puede previsualizar&nbsp; el art&iacute;culo haciendo click en&nbsp; el &iacute;cono <img src="../icon/icon-view.gif" alt="previsualizar"></p>
 <p class="help-subtitulo">2.-&nbsp; Colocando  im&aacute;genes  alojadas en <span class="rojo">cualquier</span> servidor</p>
<p>En general usted puede usar cualquier imagen ubicada en un servidor web. <a href="http://paginainteligente.net/SV-detalle-articulo.php?id=40" target="_blank">Usted puede  utilizar su propio servidor web para cargar todo tipo de&nbsp; archivos</a> (incluyendo imagenes) sin utilizar el manejador de contenidos; pero tambien puede &quot; robar&quot; im&aacute;genes de otros sitios web, mientras que&nbsp; el servidor lo permita ( <a href="http://www.webtaller.com/construccion/lenguajes/php/lecciones/proteccion-contra-hotlinking.php" target="_blank">protecci&oacute;n contra hotlink</a> ). La parte &quot;complicada&quot;&nbsp; es saber el URL o ubicaci&oacute;n de la imagen (comienza por&nbsp; http:// y termina en .jpg o .gif o .png ... es algo como&nbsp; &quot; <em>http://www.tupagina.com/imagenes/imagen.jpg</em> &quot; ) </p>
 <p class="help-subtitulo2">Si usted todav&iacute;a usa Internet Explorer </p>

<ol>
 <li>
   Es necesario saber el <a class="help-link-explicativo" title="la ubicación exacta en internet de un objeto; comienza siempre por http://...">&quot;url&quot;</a>&nbsp; de
   la misma , para ello vaya al Web Site original y toque la imagen con el boton derecho, vaya a &quot;propiedades&quot;. </li>
 <li>
Copie la ubicaci&oacute;n completa de la imagen (deberia verse algo como http://www.pagina.com/imagen.jpg). </li>
 <li>
  Venga al editor de texto y seleccione el botonsito de imagen <img src="imagen-botonsito.jpg" width="15" height="15" align="absmiddle"> y escriba la direcci&oacute;n que acaba de copiar en el
   campo &quot; Direcci&oacute;n de la imagen&quot;. </li>
 <li>
Proceda a acomodar la imagen a sus necesidades siguiendo las instrucciones del di&aacute;logo de
   imagen del editor. </li>
</ol>

 <p class="help-subtitulo2">Si usted  usa Firefox o Chrome</p>
 <ol>
 <li>
 Ubique la imagen que desea utilizar y cliqu&eacute;ela con el bot&oacute;n derecho, seleccione &quot; Ver imagen&quot; o &quot;View Image&quot;. </li>
 <li>
 Una vez que la imagen est&eacute; en una ventana o tab nuevo, ella sola, seleecione todo (CTRL A) y copie (CTRL C)</li>
 <li>
 Venga al editor de texto, haga click en el espacio vac&iacute;o y pegue (CTRL V). </li>
 <li>
 Formatee la imagen seg&uacute;n sus necesidades en el botonsito de imagen <img src="imagen-botonsito.jpg" alt="imagen" width="15" height="15" align="absmiddle"> en el editor.</li>
</ol>









